# Função de soma
def soma(n1, n2):
    return n1 + n2

# Função de subtração
def subtracao(n1, n2):
    return n1 - n2

# Função de multiplicação
def multiplicacao(n1, n2):
    return n1 * n2

# Função de divisão
def divisao(n1, n2):
    return n1 / n2

x = int(input("Digite o 1º número: "))
y = int(input("Digite o 2º número: "))

print(soma(x, y))
print(subtracao(x, y))
print(divisao(x, y))
print(multiplicacao(x, y))
